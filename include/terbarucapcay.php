								<h3>SEJARAH CAPCAY</h3>
								
								<p> Dari namanya saja kita sudah dapat menebak ya, kalau capcay itu berasal dari China. Dalam bahasa Hokkian, Cap artinya sepuluh dan cay artinya sayuran. Jadi dahulu capcay berisi 10 macam sayuran yang diolah dengan bumbu rempah yang gurih.
Dilansir dari RRI, capcay sudah ada sejak masa Dinasti Qing di China. Saat itu, aneka sayuran dimasak bersama dengan jeroan sebagai makanan sehat bagi warga setempat.
Namun sumber lain menyebutkan, capcay merupakan olahan makanan sisa yang dibuat oleh koki kerajaan. "Di zaman dinasti, raja tidak mau makan makanan sisa, namun daripada dibuang koki berinisiatif mencampurkan aneka sayuran itu menjadi masakan baru," terang pakar kuliner pernakan China, Aji Bromokusumo seperti dikutip dari Kompas.
Selanjutnya, capcay dikenalkan di Indonesia oleh imigran. Masih menurut Aji, para imigran yang tiba di Indonesia memasak capcay karena negeri ini kaya sayuran. Selain itu, bagi imigran, daging di Indonesia masih terlalu mahal.
