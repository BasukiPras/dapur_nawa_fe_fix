								<h3>TIPS MENGGORENG MAKANAN AGAR TIDAK TERLALU  BERBAHAYA BAGI KESEHATAN</h3>
								
								<p> Hampir setiap orang menyukai makanan yang digoreng, apalagi gorengan seperti bakwan, risol, cireng dan lainnya. Namun dibalik kelezatannya, gorengan memang tidak dianjurkan dikonsumsi berlebihan karena bisa berpengaruh buruk bagi kesehatan. Untuk mengurangi dampak memakan gorengan, berikut ini beberapa tips menggoreng untuk mengurangi dampak buruk yang diterima oleh tubuh, mengutip dari Southern Kitchen.
Pakai minyak zaitun
Lebih baik menggunakan minyak zaitun (olive oil) untuk menggoreng karena memiliki lemak jenuh yang sedikit
Jangan pakai api yang besar
Panaskan minyak dan menggorenglah dengan suhu yang tidak terlalu panas dan tidak terlalu rendah. Ini karena semakin besar api, maka semakin banyak minyak yang meresap ke dalam makanan. Selain itu, jangan celupkan makanan sebelum minyak mendidih.
Lapisi dengan tepung beras
Jika tidak ingin makanan menyerap lebih banyak minyak, lapisi makanan dengan tepung beras sebelum digoreng.
Pakai towel paper
Gunakan tisu makanan yang tebal dan permukaannya kasar (towel/kitchen paper) pada makanan yang terlalu berminyak, baik sebagai alas maupun untuk menepuk-nepuk permukaan makanan agar minyaknya bisa terserap.
Gunakan minyak baru
Dilarang menggunakan minyak yang telah digunakan untuk menggoreng kembali, karena hal itu akan meningkatkan kolesterol jahat dalam tubuh.
Jangan berlebihan
Yang utama tentu kamu harus ingat bahwa jangan sampai terlalu berlebihan mengonsumsi makanan yang digoreng.
